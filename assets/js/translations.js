class LanguageSwitcher {
  constructor() {
    this.currentLang = localStorage.getItem("language") || "en";
    this.init();
  }

  init() {
    this.setLanguage(this.currentLang);

    const toggleSwitches = document.querySelectorAll("[data-lang-toggle]");
    const langLabels = document.querySelectorAll("[data-lang-label]");

    toggleSwitches.forEach((toggleBtn) => {
      toggleBtn.addEventListener("click", (e) => {
        e.preventDefault();
        e.stopPropagation();
        const newLang = this.currentLang === "en" ? "uk" : "en";
        this.setLanguage(newLang);
      });
    });

    langLabels.forEach((label) => {
      label.addEventListener("click", (e) => {
        e.preventDefault();
        e.stopPropagation();
        const lang = label.getAttribute("data-lang-label");
        this.setLanguage(lang);
      });
    });
  }

  setLanguage(lang) {
    this.currentLang = lang;
    localStorage.setItem("language", lang);

    this.updateToggleUI();
    this.translatePage();

    document.dispatchEvent(
      new CustomEvent("languageChanged", { detail: { lang } })
    );
  }

  updateToggleUI() {
    const toggleSwitches = document.querySelectorAll("[data-lang-toggle]");
    const langLabels = document.querySelectorAll("[data-lang-label]");

    toggleSwitches.forEach((toggleSwitch) => {
      if (this.currentLang === "en") {
        toggleSwitch.classList.add("active");
      } else {
        toggleSwitch.classList.remove("active");
      }
    });

    langLabels.forEach((label) => {
      const labelLang = label.getAttribute("data-lang-label");
      if (labelLang === this.currentLang) {
        label.classList.add("active");
      } else {
        label.classList.remove("active");
      }
    });
  }

  translatePage() {
    const elements = document.querySelectorAll("[data-i18n]");

    elements.forEach((element) => {
      const key = element.getAttribute("data-i18n");
      if (!key || !translations[this.currentLang]) return;
      
      const translation = translations[this.currentLang][key];

      if (translation) {
        if (element.hasAttribute("data-i18n-html")) {
          element.innerHTML = translation;
        } else {
          element.textContent = translation;
        }
      }
    });

    const placeholderElements = document.querySelectorAll(
      "[data-i18n-placeholder]"
    );
    placeholderElements.forEach((element) => {
      const key = element.getAttribute("data-i18n-placeholder");
      const translation = translations[this.currentLang][key];

      if (translation) {
        element.setAttribute("placeholder", translation);
      }
    });

    const ariaElements = document.querySelectorAll("[data-i18n-aria]");
    ariaElements.forEach((element) => {
      const key = element.getAttribute("data-i18n-aria");
      const translation = translations[this.currentLang][key];

      if (translation) {
        element.setAttribute("aria-label", translation);
      }
    });

    const selectOptions = document.querySelectorAll("select option[data-i18n]");
    selectOptions.forEach((option) => {
      const key = option.getAttribute("data-i18n");
      const translation = translations[this.currentLang][key];
      if (translation) {
        option.textContent = translation;
      }
    });

    const partySizeSelect = document.querySelector('select[name="partySize"]');
    if (partySizeSelect) {
      const partySizeOptions =
        partySizeSelect.querySelectorAll("option[value]");
      partySizeOptions.forEach((option) => {
        const value = option.getAttribute("value");
        if (value && value !== "") {
          const key =
            value === "8-plus"
            ? "reservation2.partySize.8plus" 
            : `reservation2.partySize.${value}`;
          const translation = translations[this.currentLang][key];
          if (translation) {
            option.textContent = translation;
          }
        }
      });
    }
  }

  getCurrentLanguage() {
    return this.currentLang;
  }
}

